let%expect_test "[merge]" =
  let test b1 b2 =
    print_s
      [%message
        ""
          ~_:(b1 : Background.t)
          ~_:(b2 : Background.t)
          ~merge:(Attribute.merge Background b1 b2 : Background.t)]
  in
  test (Color Color.red) Unspecified;
  [%expect {| ((Color red) Unspecified (merge (Color red))) |}];
  test (Color Color.red) (Color Color.red);
  [%expect
    {|
    ((Color red)
     (Color red)
     (merge (Color red)))
    |}];
  test (Color Color.red) (Color Color.black);
  [%expect
    {|
    ((Color red)
     (Color black)
     (merge (Color red)))
    |}];
  test (Color Color.black) (Color Color.red);
  [%expect
    {|
    ((Color black)
     (Color red)
     (merge (Color black)))
    |}];
  return ()
;;

